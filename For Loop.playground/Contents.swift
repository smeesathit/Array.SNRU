import UIKit

// Implicit declaration
let intNumber = [11, 22, 33, 44, 55]
var intIndex = 0

// Basic of "for loop"
for intArray in intNumber {
    print("intArray ==> \(intIndex) = \(intArray)")
    intIndex += 1
    
}

// for loop with String
let strArrayName = ["AAA", "BBB", "CCC", "DDD"]
for strMyName in strArrayName {
    print("strMyName ==> \(strMyName)")
}
